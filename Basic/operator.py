# Arithmetic
a = 10
b = 5
print(a + b)  # Add
print(a - b)  # Subtract
print(a * b)  # Multiplication
print(a / b)  # Division
print(a % b)  # Modulus
print(a ** b)  # Exponential

# Assignment
c = a+b
c += b
print(c)

# Relational
a = 10
b = 3
print(a > b)
print(a < b)

# Logical
a = 10
b = 5
c = a > 3 or b < 5
d = a > 3 and b < 5
print(c, d)

# Unary
p = 5
p = -p
print(p)
